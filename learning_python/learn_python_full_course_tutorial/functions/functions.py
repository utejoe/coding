def say_hi(name, age): #creating a function say_hi giving a parameter name and age
    print("Hello " + name) # a print function with parameter name alone
    print ("hello " +name+ ", you are " +age+ " years old") # a print function with parameter name and age
    
say_hi("Mike", '35') #calling the function say_hi with parameter name as Mike
say_hi("Steve", '70') #calling the function say_hi with parameter name as Steve and age as 70
