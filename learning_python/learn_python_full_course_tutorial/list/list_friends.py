friends = ['Kelvin', "Karen", "Jim", 'Oscar', "Toby"] # a list of friends
print(friends)
print(friends[1]) #printing a particular elements using index numbers. NB: indexing starts from 0.
print(friends[-2]) #printing a particular element using -ve indexing which starts from the ending of the line
print(friends[1:]) #printing some elements from the list and ignoring some of them
print(friends[1:4]) #printing some elements from the list base on a particular range
friends[3] = "Jude" #to modify an element base on the index number
print(friends)